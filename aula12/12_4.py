a = '   Laboratórios de Informática   '
print(len(a))       #Comprimento de uma string
print(a.lower())    #Converter para minusculas
print(a.upper())    #Converter para maiusculas
print(a.title())    #Converter para título
print(a.find('t'))  #Primeira posição de t
print(a.isalpha())  #Verifica se só tem maiusculas
print(a.isdigit())  #Verifica se só tem digitos
print(a.islower())  #Verifica se so tem minusculas
print(a.isupper())  #Verifica se so tem maiusculas
print(a.strip())    #Remove os espaços nos extremos
print(a.split(' ')) #Divide por espaços